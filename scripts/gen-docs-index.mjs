#!/usr/bin/env node
// Regenerates src/docs-index.ts from svelte.dev's content index.
// Run `npm run gen:docs` when svelte.dev adds or renames doc pages.
import { writeFile } from "node:fs/promises";

const res = await fetch("https://svelte.dev/content.json");
if (!res.ok) {
  console.error(`fetch failed: ${res.status}`);
  process.exit(1);
}
const { blocks } = await res.json();

const pages = new Map();
for (const block of blocks) {
  const href = block.href.split("#")[0];
  const match = href.match(/^\/docs\/([^/]+)\/(.+)$/);
  if (!match || pages.has(href)) continue;
  pages.set(href, {
    pkg: match[1],
    slug: match[2],
    title: block.breadcrumbs.at(-1),
  });
}

const rows = [...pages.values()];
const body = rows
  .map((r) => `  { pkg: ${JSON.stringify(r.pkg)}, slug: ${JSON.stringify(r.slug)}, title: ${JSON.stringify(r.title)} },`)
  .join("\n");

const out = `// Generated by scripts/gen-docs-index.mjs — do not edit by hand.
// Source: https://svelte.dev/content.json (${new Date().toISOString().slice(0, 10)})

export interface DocSection {
  pkg: string;
  slug: string;
  title: string;
}

export const DOC_SECTIONS: DocSection[] = [
${body}
];
`;

await writeFile(new URL("../src/docs-index.ts", import.meta.url), out);
console.error(`wrote ${rows.length} sections to src/docs-index.ts`);
